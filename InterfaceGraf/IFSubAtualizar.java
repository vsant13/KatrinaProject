    private final Facede f = FacedeHighlander.getInstance();
    
    private void initCiaText(){
        
        int i = 0;
        String temp[] , text = "";
        temp = f.getCias();
        while( i < temp.length){    
            text += "    " + temp[i] + "\n";
        taResultado.setText(text);
            i++;
        }
        taResultado.setEditable(false);
    }
    

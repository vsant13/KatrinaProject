    @Override
    public void lockGUI(){
        btBovespa.setEnabled(false);
        btCVM.setEnabled(false);
        btTesouro.setEnabled(false);
        IFControlDB.lockGUI();
    }
    @Override
    public void freeGUI(){
        btBovespa.setEnabled(true);
        btCVM.setEnabled(true);
        btTesouro.setEnabled(true);
        IFControlDB.freeGUI();
    }
    
    private class AtualizarBmf implements Runnable{

         private AtualizarBmf(){
              UserDB.getUserDB();
         }
         private void atualiza(){
                   lockGUI();
                   lbComentario.setText("...");
                   taResultado.setText("Preparando Cotação ...");
                   taResultado.setForeground(Color.RED);
                   f.bovespaRotine();
                   lbComentario.setText("Atualização da Cotacão da Bovespa Concluída .");
                   taResultado.setText("Cotação Atualizada");
                   taResultado.setForeground(Color.GREEN);
                   freeGUI();
         }
         
        @Override
        public void run() {
            atualiza();
        }
    
    }
    
     private class AtualizarCvm implements Runnable{

         private AtualizarCvm(){
              UserDB.getUserDB();
         }
         private void atualiza(){
                   lockGUI();
                   lbComentario.setText("...");
                   taResultado.setText("Preparando ITR/DFP ...");
                   taResultado.setForeground(Color.RED);
                   f.atualizaCias();
                   lbComentario.setText("Atualização da Cotacão da CVM Concluída .");
                   lbComentario.repaint();
                   taResultado.setText("Informações Atualizada");
                   taResultado.setForeground(Color.GREEN);
                   freeGUI();
         }
         
        @Override
        public void run() {
            atualiza();
        }
         
     
     }
    
    
    
